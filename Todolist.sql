CREATE TABLE TB_TODO(
	CATEGORY CHAR(2) PRIMARY KEY,
	TODO VARCHAR2(30),
	DETAILS VARCHAR2(30),
	SET_DATE DATE DEFAULT SYSDATE,
	STATUS CHAR(1) DEFAULT 'N' CHECK(STATUS IN('N', 'Y'))
);

