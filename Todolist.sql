CREATE TABLE TB_MEMBER(
	MEMBER_NO NUMBER PRIMARY KEY,
	MEMBER_NAME VARCHAR2(15) NOT NULL,
	TITLE_CODE CHAR(2) REFERENCES TB_TODO,
